package fa.training.assignment1;

public class LogicalExercise {

	public static void main(String[] args) {
		int x = 25;
		int y = 39;
		if (x != y) {
			System.out.println(x + " != " + y);
		}
		if (x <= y) {
			System.out.println(x + " <= " + y);
		}
		if (x < y) {
			System.out.println(x + " < " + y);
		}
		if (x >= y) {
			System.out.println(x + " >= " + y);
		}
		if (x > y) {
			System.out.println(x + " > " + y);
		}
	}

}
